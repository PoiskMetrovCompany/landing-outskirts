const AboutTabs = () => {
  const [activeTabId, setActiveTabId] = useState<AboutTabId>("project")

  const activeTab =
    ABOUT_TABS.find((tab) => tab.id === activeTabId) ?? ABOUT_TABS[0]

  return (
    <section
      id="about"
      className={styles.aboutTabs}
      aria-labelledby="about-tabs-title"
    >
      <header className={styles.aboutTabs__header}>
        <h2 id="about-tabs-title" className={styles.aboutTabs__heading}>
          О ПРОЕКТЕ
        </h2>

        <div className={styles.aboutTabs__controls}>
          <nav
            className={styles.aboutTabs__tabs}
            aria-label="Разделы о проекте"
          >
            {ABOUT_TABS.map((tab) => (
              <button
                key={tab.id}
                type="button"
                className={clsx(
                  styles.aboutTabs__tab,
                  tab.id === activeTabId && styles["aboutTabs__tab--active"],
                )}
                onClick={() => setActiveTabId(tab.id)}
                aria-current={tab.id === activeTabId ? "true" : undefined}
              >
                {tab.label}
              </button>
            ))}
          </nav>

          <Button
            className={styles.aboutTabs__cta}
            color="yellow"
            href="/catalogue"
          >
            Выбрать квартиру
          </Button>
        </div>
      </header>

      <div className={styles.aboutTabs__content}>
        <article className={styles.aboutTabs__textCard}>
          <div className={styles.aboutTabs__textWrap}>
            <h3 className={styles.aboutTabs__title}>{activeTab.title}</h3>

            <div className={styles.aboutTabs__text}>
              {activeTab.paragraphs.map((paragraph) => (
                <p key={paragraph}>{paragraph}</p>
              ))}
            </div>
          </div>

          <a href="#ceo" className={styles.aboutTabs__link}>
            Читать подробнее
          </a>
        </article>

        <div className={styles.aboutTabs__imageCard}>
          <Image
            src={activeTab.image}
            alt={activeTab.imageAlt}
            fill
            priority={false}
            className={styles.aboutTabs__image}
          />
        </div>
      </div>
    </section>
  )
}

export default AboutTabs
